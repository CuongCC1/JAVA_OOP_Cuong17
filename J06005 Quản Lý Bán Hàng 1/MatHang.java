public class MatHang {
    private String maMH ,tenMH ,donViTinh ;
    private long giaMua,giaBan;

    public MatHang(int maMH, String tenMH, String donViTinh, long giaMua, long giaBan) {
        this.maMH = "MH"+String.format("%03d",maMH);
        this.tenMH = tenMH;
        this.donViTinh = donViTinh;
        this.giaMua = giaMua;
        this.giaBan = giaBan;
    }

    public String getMaMH() {
        return maMH;
    }

    public void setMaMH(String maMH) {
        this.maMH = maMH;
    }

    public String getTenMH() {
        return tenMH;
    }

    public void setTenMH(String tenMH) {
        this.tenMH = tenMH;
    }

    public String getDonViTinh() {
        return donViTinh;
    }

    public void setDonViTinh(String donViTinh) {
        this.donViTinh = donViTinh;
    }

    public long getGiaMua() {
        return giaMua;
    }

    public void setGiaMua(long giaMua) {
        this.giaMua = giaMua;
    }

    public long getGiaBan() {
        return giaBan;
    }

    public void setGiaBan(long giaBan) {
        this.giaBan = giaBan;
    }
    
}
